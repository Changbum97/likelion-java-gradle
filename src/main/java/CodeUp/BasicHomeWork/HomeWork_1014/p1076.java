package CodeUp.BasicHomeWork.HomeWork_1014;

import java.util.Scanner;

public class p1076 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        char in = sc.nextLine().charAt(0);
        for(char c = 'a' ; c <= in ; c ++) {
            System.out.print(c + " ");
        }
    }
}
